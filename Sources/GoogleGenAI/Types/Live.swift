/// Response message for API call.
public final class LiveServerMessage: Codable, @unchecked Sendable {
    /// Sent in response to a `LiveClientSetup` message from the client.
    public var setupComplete: LiveServerSetupComplete?
    /// Content generated by the model in response to client messages.
    public var serverContent: LiveServerContent?
    /// Request for the client to execute the `function_calls`.
    public var toolCall: LiveServerToolCall?
    /// Notification for the client that a previously issued tool call should be cancelled.
    public var toolCallCancellation: LiveServerToolCallCancellation?
    /// Usage metadata about model response(s).
    public var usageMetadata: UsageMetadata?
    /// Server will disconnect soon.
    public var goAway: LiveServerGoAway?
    /// Update of the session resumption state.
    public var sessionResumptionUpdate: LiveServerSessionResumptionUpdate?
    /// Voice activity detection signal. Allowlisted only.
    public var voiceActivityDetectionSignal: VoiceActivityDetectionSignal?
    /// Voice activity signal.
    public var voiceActivity: VoiceActivity?

    public init(
        setupComplete: LiveServerSetupComplete? = nil,
        serverContent: LiveServerContent? = nil,
        toolCall: LiveServerToolCall? = nil,
        toolCallCancellation: LiveServerToolCallCancellation? = nil,
        usageMetadata: UsageMetadata? = nil,
        goAway: LiveServerGoAway? = nil,
        sessionResumptionUpdate: LiveServerSessionResumptionUpdate? = nil,
        voiceActivityDetectionSignal: VoiceActivityDetectionSignal? = nil,
        voiceActivity: VoiceActivity? = nil
    ) {
        self.setupComplete = setupComplete
        self.serverContent = serverContent
        self.toolCall = toolCall
        self.toolCallCancellation = toolCallCancellation
        self.usageMetadata = usageMetadata
        self.goAway = goAway
        self.sessionResumptionUpdate = sessionResumptionUpdate
        self.voiceActivityDetectionSignal = voiceActivityDetectionSignal
        self.voiceActivity = voiceActivity
    }

    /// Returns the concatenation of all text parts from the server content if present.
    public var text: String? {
        var result = ""
        var anyTextPartFound = false
        var nonTextParts: [String] = []
        for part in serverContent?.modelTurn?.parts ?? [] {
            if part.inlineData != nil { nonTextParts.append("inlineData") }
            if part.fileData != nil { nonTextParts.append("fileData") }
            if part.functionCall != nil { nonTextParts.append("functionCall") }
            if part.functionResponse != nil { nonTextParts.append("functionResponse") }
            if part.executableCode != nil { nonTextParts.append("executableCode") }
            if part.codeExecutionResult != nil { nonTextParts.append("codeExecutionResult") }
            if part.videoMetadata != nil { nonTextParts.append("videoMetadata") }
            if let textValue = part.text {
                if part.thought == true { continue }
                anyTextPartFound = true
                result += textValue
            }
        }
        if !nonTextParts.isEmpty {
            // swift-genai: parity with JS console.warn
            print("there are non-text parts \(nonTextParts) in the response, returning concatenation of all text parts.")
        }
        return anyTextPartFound ? result : nil
    }

    /// Returns the concatenation of all inline data parts from the server content if present.
    public var data: String? {
        var raw = Data()
        var nonDataParts: [String] = []
        for part in serverContent?.modelTurn?.parts ?? [] {
            if part.text != nil { nonDataParts.append("text") }
            if part.fileData != nil { nonDataParts.append("fileData") }
            if part.functionCall != nil { nonDataParts.append("functionCall") }
            if part.functionResponse != nil { nonDataParts.append("functionResponse") }
            if part.executableCode != nil { nonDataParts.append("executableCode") }
            if part.codeExecutionResult != nil { nonDataParts.append("codeExecutionResult") }
            if part.videoMetadata != nil { nonDataParts.append("videoMetadata") }
            if let inline = part.inlineData, let str = inline.data,
               let decoded = Data(base64Encoded: str) {
                raw.append(decoded)
            }
        }
        if !nonDataParts.isEmpty {
            print("there are non-data parts \(nonDataParts) in the response, returning concatenation of all data parts.")
        }
        return raw.isEmpty ? nil : raw.base64EncodedString()
    }
}


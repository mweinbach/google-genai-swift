/// Callbacks for the realtime API.
///
/// - Note: Not `Codable` — function-typed members can't be encoded/decoded.
public struct LiveCallbacks: Sendable {
    /// Called when a message is received from the server.
    public var onMessage: @Sendable (LiveServerMessage) -> Void
    /// Called when an error occurs.
    public var onError: (@Sendable (Error) -> Void)?
    /// Called when the websocket connection is opened.
    public var onOpen: (@Sendable () -> Void)?
    /// Called when the websocket connection is closed.
    public var onClose: (@Sendable (Error?) -> Void)?

    public init(
        onMessage: @escaping @Sendable (LiveServerMessage) -> Void,
        onError: (@Sendable (Error) -> Void)? = nil,
        onOpen: (@Sendable () -> Void)? = nil,
        onClose: (@Sendable (Error?) -> Void)? = nil
    ) {
        self.onMessage = onMessage
        self.onError = onError
        self.onOpen = onOpen
        self.onClose = onClose
    }
}

